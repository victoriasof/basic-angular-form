export class Friend {

  fname: string;
  lname: string;
  email: string;
  phoneNumber: string;
  language: string;

  constructor(fname:string, lname:string, email:string, phoneNumber: string, language:string) {

    this.fname = fname;
    this.lname = lname;
    this.email = email;
    this.phoneNumber = phoneNumber;
    this.language = language;

  }
}
